    def test_parse_removed_block(self):
        """Parse a removed {} block with nested lifecycle."""
        lifecycle_block = HclBlock(
            block_type="lifecycle",
            labels=[],
            attributes={"destroy": True},
        )
        removed_block = HclBlock(
            block_type="removed",
            labels=[],
            attributes={
                "from": HclRawExpression("aws_ebs_volume.vol_abc123"),
            },
            nested_blocks=[lifecycle_block],
        )
        doc = HclDocument(blocks=[removed_block])
        rendered = doc.render()
        parsed = HclDocument.parse(rendered)

        assert len(parsed.blocks) == 1
        assert parsed.blocks[0].block_type == "removed"
        from_val = parsed.blocks[0].attributes["from"]
        assert isinstance(from_val, HclRawExpression)
        assert from_val.expression == "aws_ebs_volume.vol_abc123"
        assert len(parsed.blocks[0].nested_blocks) == 1
        assert parsed.blocks[0].nested_blocks[0].block_type == "lifecycle"
        assert parsed.blocks[0].nested_blocks[0].attributes["destroy"] is True

    def test_parse_full_resize_ec2_document(self):
        """Parse a complete resize EC2 document (realistic scenario)."""
        from hcl_generator.actions import generate_action_hcl

        doc = generate_action_hcl(
            action_type="resize-ec2",
            action_params={
                "instanceId": "i-0abc123def",
                "targetInstanceType": "t3.medium",
            },
            account_id="123456789012",
            region="us-east-1",
        )
        rendered = doc.render()
        parsed = HclDocument.parse(rendered)

        # Should have header comment
        assert "Generated by SlashMyBill" in parsed.header_comment
        assert "Resize EC2" in parsed.header_comment

        # Should have terraform, provider, resource, and import blocks
        block_types = [b.block_type for b in parsed.blocks]
        assert "terraform" in block_types
        assert 'provider "aws"' in block_types
        assert "resource" in block_types
        assert "import" in block_types

        # Find the resource block
        resource_block = next(b for b in parsed.blocks if b.block_type == "resource")
        assert resource_block.labels[0] == "aws_instance"
        assert resource_block.attributes["instance_type"] == "t3.medium"


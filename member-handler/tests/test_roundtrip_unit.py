class TestActionDefinitionToHcl:
    """Tests for ActionDefinition.to_hcl()."""

    def test_resource_action_generates_resource_and_import(self):
        """A non-removal action generates resource + import blocks."""
        action = ActionDefinition(
            action_type="resize-ec2",
            resource_type="aws_instance",
            resource_id="i-0abc123def",
            attributes={"instance_type": "t3.medium"},
            import_id="i-0abc123def",
            is_removal=False,
            tags=None,
        )
        doc = action.to_hcl()
        rendered = doc.render()

        assert 'resource "aws_instance"' in rendered
        assert 'instance_type = "t3.medium"' in rendered
        assert "import {" in rendered
        assert 'id = "i-0abc123def"' in rendered

    def test_removal_action_generates_removed_block(self):
        """A removal action generates a removed {} block."""
        action = ActionDefinition(
            action_type="delete-ebs",
            resource_type="aws_ebs_volume",
            resource_id="vol-0abc123",
            attributes={},
            import_id=None,
            is_removal=True,
            tags=None,
        )
        doc = action.to_hcl()
        rendered = doc.render()

        assert "removed {" in rendered
        assert "aws_ebs_volume" in rendered
        assert "lifecycle {" in rendered
        assert "destroy = true" in rendered
        # Should NOT have a resource block
        assert 'resource "aws_ebs_volume"' not in rendered

    def test_action_with_tags(self):
        """Tags are included in the resource block."""
        action = ActionDefinition(
            action_type="apply-tags",
            resource_type="aws_instance",
            resource_id="i-0abc123",
            attributes={},
            import_id="i-0abc123",
            is_removal=False,
            tags={"Name": "my-server", "Environment": "prod"},
        )
        doc = action.to_hcl()
        rendered = doc.render()

        assert "tags = {" in rendered
        assert 'Name = "my-server"' in rendered
        assert 'Environment = "prod"' in rendered

    def test_action_without_import_id(self):
        """An action without import_id doesn't generate an import block."""
        action = ActionDefinition(
            action_type="create-budget",
            resource_type="aws_budgets_budget",
            resource_id="my-budget",
            attributes={"name": "my-budget", "budget_type": "COST"},
            import_id=None,
            is_removal=False,
            tags=None,
        )
        doc = action.to_hcl()
        rendered = doc.render()

        assert 'resource "aws_budgets_budget"' in rendered
        assert "import {" not in rendered



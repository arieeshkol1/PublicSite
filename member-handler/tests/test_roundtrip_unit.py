    def test_parse_nested_blocks(self):
        """Parse blocks with nested blocks."""
        inner = HclBlock(
            block_type="assume_role",
            attributes={"role_arn": "arn:aws:iam::123456789012:role/MyRole"},
        )
        outer = HclBlock(
            block_type='provider "aws"',
            attributes={"region": "us-east-1"},
            nested_blocks=[inner],
        )
        doc = HclDocument(blocks=[outer])
        rendered = doc.render()
        parsed = HclDocument.parse(rendered)

        assert len(parsed.blocks) == 1
        assert parsed.blocks[0].block_type == 'provider "aws"'
        assert parsed.blocks[0].attributes["region"] == "us-east-1"
        assert len(parsed.blocks[0].nested_blocks) == 1
        assert parsed.blocks[0].nested_blocks[0].block_type == "assume_role"
        assert parsed.blocks[0].nested_blocks[0].attributes["role_arn"] == (
            "arn:aws:iam::123456789012:role/MyRole"
        )

    def test_parse_raw_expression(self):
        """Parse raw expressions (unquoted values)."""
        doc = HclDocument(blocks=[
            HclBlock(
                block_type="import",
                labels=[],
                attributes={
                    "to": HclRawExpression("aws_instance.main"),
                    "id": "i-0abc123",
                },
            )
        ])
        rendered = doc.render()
        parsed = HclDocument.parse(rendered)

        assert parsed.blocks[0].block_type == "import"
        # Raw expression should be parsed back as HclRawExpression
        to_val = parsed.blocks[0].attributes["to"]
        assert isinstance(to_val, HclRawExpression)
        assert to_val.expression == "aws_instance.main"
        # String value should be parsed as string
        assert parsed.blocks[0].attributes["id"] == "i-0abc123"

    def test_parse_multiple_blocks(self):
        """Parse a document with multiple top-level blocks."""
        doc = HclDocument(blocks=[
            HclBlock(block_type="terraform"),
            HclBlock(
                block_type='provider "aws"',
                attributes={"region": "us-east-1"},
            ),
            HclBlock(
                block_type="resource",
                labels=["aws_instance", "main"],
                attributes={"instance_type": "t3.medium"},
            ),
        ])
        rendered = doc.render()
        parsed = HclDocument.parse(rendered)

        assert len(parsed.blocks) == 3
        assert parsed.blocks[0].block_type == "terraform"
        assert parsed.blocks[1].block_type == 'provider "aws"'
        assert parsed.blocks[2].block_type == "resource"
        assert parsed.blocks[2].labels == ["aws_instance", "main"]


"""Optimization action HCL generators for Terraform code generation.

Dispatches action_type to individual generator functions that produce
valid Terraform HCL for each supported optimization action.

Supported action types:
- resize-ec2: Resize an EC2 instance to a recommended type
- delete-ebs: Delete an unused EBS volume
- release-eip: Release an unused Elastic IP
- s3-lifecycle: Add lifecycle rules to an S3 bucket
- create-schedule: Create start/stop schedules for resources
- apply-tags: Apply recommended tags to a resource
- create-budget: Create a budget with notification thresholds
"""

from __future__ import annotations

from datetime import datetime, timezone

from hcl_generator.core import HclBlock, HclDocument, HclRawExpression, render_provider_block
from hcl_generator.identifiers import to_terraform_identifier


SUPPORTED_ACTION_TYPES: set[str] = {
    "resize-ec2",
    "delete-ebs",
    "release-eip",
    "s3-lifecycle",
    "create-schedule",
    "apply-tags",
    "create-budget",
}


def _build_header_comment(
    action_description: str,
    account_id: str,
    timestamp: datetime | None = None,
) -> str:
    """Build the standard header comment for generated action HCL files.

    Args:
        action_description: Human-readable description of the action.
        account_id: Target AWS account ID.
        timestamp: Generation timestamp. Defaults to current UTC time.

    Returns:
        Multi-line header comment string.
    """
    if timestamp is None:
        timestamp = datetime.now(timezone.utc)

    iso_timestamp = timestamp.strftime("%Y-%m-%dT%H:%M:%SZ")

    lines = [
        "Generated by SlashMyBill - Terraform IaC Integration",
        f"Action: {action_description}",
        f"Account: {account_id}",
        f"Generated: {iso_timestamp}",
        "WARNING: Review this file before applying with terraform apply",
    ]
    return "\n".join(lines)


def _action_description(action_type: str, action_params: dict) -> str:
    """Build a human-readable description for the action header comment.

    Args:
        action_type: The optimization action type.
        action_params: Action-specific parameters.

    Returns:
        A description string for the header comment.
    """
    descriptions = {
        "resize-ec2": lambda p: (
            f"Resize EC2 instance {p.get('instanceId', 'unknown')} "
            f"to {p.get('targetInstanceType', 'unknown')}"
        ),
        "delete-ebs": lambda p: (
            f"Delete EBS volume {p.get('volumeId', 'unknown')}"
        ),
        "release-eip": lambda p: (
            f"Release Elastic IP {p.get('allocationId', 'unknown')}"
        ),
        "s3-lifecycle": lambda p: (
            f"Add lifecycle rules to S3 bucket {p.get('bucketName', 'unknown')}"
        ),
        "create-schedule": lambda p: (
            f"Create start/stop schedule for {p.get('resourceType', 'resource')} "
            f"{p.get('resourceId', 'unknown')}"
        ),
        "apply-tags": lambda p: (
            f"Apply tags to {p.get('resourceType', 'resource')} "
            f"{p.get('resourceId', 'unknown')}"
        ),
        "create-budget": lambda p: (
            f"Create budget {p.get('budgetName', 'unknown')}"
        ),
    }
    desc_fn = descriptions.get(action_type)
    if desc_fn:
        return desc_fn(action_params)
    return f"Optimization action: {action_type}"


def generate_action_hcl(
    action_type: str,
    action_params: dict,
    account_id: str,
    region: str = "us-east-1",
) -> HclDocument:
    """Generate Terraform HCL for an optimization action.

    Validates the action type, builds a header comment, and dispatches
    to the appropriate generator function.

    Args:
        action_type: The optimization action type (must be in SUPPORTED_ACTION_TYPES).
        action_params: Action-specific parameters dict.
        account_id: Target AWS account ID (12 digits).
        region: AWS region (defaults to us-east-1).

    Returns:
        An HclDocument containing the generated Terraform configuration.

    Raises:
        ValueError: If action_type is not in SUPPORTED_ACTION_TYPES.
    """
    if action_type not in SUPPORTED_ACTION_TYPES:
        raise ValueError(
            f"Unsupported action type: '{action_type}'. "
            f"Supported types are: {sorted(SUPPORTED_ACTION_TYPES)}"
        )

    # Build header comment
    description = _action_description(action_type, action_params)
    header = _build_header_comment(description, account_id)

    # Dispatch table mapping action types to generator functions
    dispatch: dict[str, callable] = {
        "resize-ec2": _generate_resize_ec2,
        "delete-ebs": _generate_delete_ebs,
        "release-eip": _generate_release_eip,
        "s3-lifecycle": _generate_s3_lifecycle,
        "create-schedule": _generate_create_schedule,
        "apply-tags": _generate_apply_tags,
        "create-budget": _generate_create_budget,
    }

    generator_fn = dispatch[action_type]
    return generator_fn(action_params, account_id, region, header)


def _generate_resize_ec2(
    action_params: dict, account_id: str, region: str, header: str
) -> HclDocument:
    """Generate HCL for resizing an EC2 instance.

    Args:
        action_params: Must contain 'instanceId' and 'targetInstanceType'.
        account_id: Target AWS account ID.
        region: AWS region.
        header: Pre-built header comment string.

    Returns:
        HclDocument with the resize EC2 configuration.
    """
    instance_id = action_params.get("instanceId", "")
    target_instance_type = action_params.get("targetInstanceType", "")

    resource_name = to_terraform_identifier(instance_id) if instance_id else "instance"

    # terraform { required_providers {} } block
    aws_provider_source = HclBlock(
        block_type="aws",
        labels=[],
        attributes={
            "source": "hashicorp/aws",
            "version": ">= 5.0",
        },
    )
    required_providers_block = HclBlock(
        block_type="required_providers",
        labels=[],
        nested_blocks=[aws_provider_source],
    )
    terraform_block = HclBlock(
        block_type="terraform",
        labels=[],
        nested_blocks=[required_providers_block],
    )

    # provider "aws" block with assume_role
    provider_block = render_provider_block(region, account_id)

    # aws_instance resource block
    resource_block = HclBlock(
        block_type="resource",
        labels=["aws_instance", resource_name],
        attributes={
            "instance_type": target_instance_type,
        },
    )

    # import {} block
    import_block = HclBlock(
        block_type="import",
        labels=[],
        attributes={
            "to": HclRawExpression(f"aws_instance.{resource_name}"),
            "id": instance_id,
        },
    )

    doc = HclDocument(
        blocks=[terraform_block, provider_block, resource_block, import_block],
        header_comment=header,
    )
    return doc


def _generate_delete_ebs(
    action_params: dict, account_id: str, region: str, header: str
) -> HclDocument:
    """Generate HCL for deleting an EBS volume using a removed {} block.

    Uses Terraform 1.7+ removed {} block syntax to destroy the volume
    without requiring it to be in state first.

    Args:
        action_params: Must contain 'volumeId'.
        account_id: Target AWS account ID.
        region: AWS region.
        header: Pre-built header comment string.

    Returns:
        HclDocument with the delete EBS configuration.
    """
    volume_id = action_params.get("volumeId", "unknown")
    tf_name = to_terraform_identifier(volume_id)

    # terraform { required_providers {} } block
    aws_provider_source = HclBlock(
        block_type="aws",
        labels=[],
        attributes={
            "source": "hashicorp/aws",
            "version": ">= 5.0",
        },
    )
    required_providers = HclBlock(
        block_type="required_providers",
        labels=[],
        nested_blocks=[aws_provider_source],
    )
    terraform_block = HclBlock(
        block_type="terraform",
        labels=[],
        nested_blocks=[required_providers],
    )

    # provider "aws" block
    provider_block = render_provider_block(region, account_id)

    # removed {} block with lifecycle { destroy = true }
    lifecycle_block = HclBlock(
        block_type="lifecycle",
        labels=[],
        attributes={"destroy": True},
    )
    removed_block = HclBlock(
        block_type="removed",
        labels=[],
        attributes={
            "from": HclRawExpression(f"aws_ebs_volume.{tf_name}"),
        },
        nested_blocks=[lifecycle_block],
    )

    return HclDocument(
        blocks=[terraform_block, provider_block, removed_block],
        header_comment=header,
    )


